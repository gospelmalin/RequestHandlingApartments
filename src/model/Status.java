package model;

public class Status {

	//Fields
	private int id;
	private String status;

	/**
	 * @return Get status_id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id status_id
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return Get status_description
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status status_description
	 */
	public void setStatus(String status) {
		this.status = status;
	}
}
